  /*发现首页tab切换*/
        findPageTab:function (){
    var $li = $('.tabNav a');
    var $ul = $('.tabList .tabListInfo');
    $("#navcon01").css('height',$ul.eq(0).height())
    $li.click(function(){
      var $this = $(this);
      var $t = $this.index();
      $li.removeClass();
      $this.addClass('Cur');
      $ul.css('display','none');
      $ul.eq($t).css('display','block');
      $("#navcon01").css('height',$ul.eq($t).height())
    })
  },
  /*话题 tab切换*/
        topicTab:function (){
    var $li = $('.titleRight a');
    var $ul = $('.tabList .tabListInfo');

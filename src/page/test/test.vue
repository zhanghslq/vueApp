<script>
  import Swiper from 'swiper'
  export default {
    name: "test",
    data() {
     return{
       bar:{},

       clientWidth:'',
       navSum:'',
       navSlideWidth:'',
       navWidth:'',
       progress:{},
       slideProgress:{},
       topBar:{},
       startPosition:'',


     }
    },
    methods:{
        nav(){
          console.log("执行方法")

          let _this=this;
          //暂时设计每个slide大小需要一致
          let barwidth = 36 //导航粉色条的长度px
          let tSpeed = 300 //切换速度300ms
          var navSwiper = new Swiper('#nav', {
            slidesPerView: 6,
            freeMode: true,
            on: {
              init: function() {
                _this.navSlideWidth = this.slides.eq(0).css('width'); //导航字数需要统一,每个导航宽度一致
                _this.bar = this.$el.find('.bar')
                _this.bar.css('width', _this.navSlideWidth)
                _this. bar.transition(tSpeed)
                _this.navSum = this.slides[this.slides.length - 1].offsetLeft //最后一个slide的位置

                _this.clientWidth = parseInt(this.$wrapperEl.css('width')) //Nav的可视宽度
                _this.navWidth = 0
                for (var i = 0; i < this.slides.length; i++) {
                  _this.navWidth += parseInt(this.slides.eq(i).css('width'))
                }

                _this.topBar = this.$el.parents('body').find('#top') //页头

              },

            },
          });

          var pageSwiper = new Swiper('#page', {
            watchSlidesProgress: true,
            resistanceRatio: 0,
            on: {
              touchMove: function() {
                _this.progress = this.progress
                _this.bar.transition(0)
                _this.bar.transform('translateX(' + _this.navSum * _this.progress + 'px)')
                //粉色255,72,145灰色51,51,51
                for (var i = 0; i < this.slides.length; i++) {
                  var slideProgress = this.slides[i].progress
                  if (Math.abs(slideProgress) < 1) {
                    var r = Math.floor((255 - 51) * (1 - Math.pow(Math.abs(slideProgress), 2)) + 51)
                    var g = Math.floor((72 - 51) * (1 - Math.pow(Math.abs(slideProgress), 2)) + 51)
                    var b = Math.floor((145 - 51) * (1 - Math.pow(Math.abs(slideProgress), 2)) + 51)
                    navSwiper.slides.eq(i).find('span').css('color', 'rgba(' + r + ',' + g + ',' + b + ',1)')
                  }
                }
              },
              transitionStart: function() {
                var activeIndex = this.activeIndex
                var  activeSlidePosition = navSwiper.slides[activeIndex].offsetLeft
                //释放时导航粉色条移动过渡
                _this.bar.transition(tSpeed)
                _this.bar.transform('translateX(' + activeSlidePosition + 'px)')
                //释放时文字变色过渡
                navSwiper.slides.eq(activeIndex).find('span').transition(tSpeed)
                navSwiper.slides.eq(activeIndex).find('span').css('color', 'rgba(255,72,145,1)')
                if (activeIndex > 0) {
                  navSwiper.slides.eq(activeIndex - 1).find('span').transition(tSpeed)
                  navSwiper.slides.eq(activeIndex - 1).find('span').css('color', 'rgba(51,51,51,1)')
                }
                if (activeIndex < this.slides.length) {
                  navSwiper.slides.eq(activeIndex + 1).find('span').transition(tSpeed)
                  navSwiper.slides.eq(activeIndex + 1).find('span').css('color', 'rgba(51,51,51,1)')
                }
                //导航居中
                var navActiveSlideLeft = navSwiper.slides[activeIndex].offsetLeft //activeSlide距左边的距离

                navSwiper.setTransition(tSpeed)
                if (navActiveSlideLeft < (_this.clientWidth - parseInt(_this.navSlideWidth)) / 2) {
                  navSwiper.setTranslate(0)
                } else if (navActiveSlideLeft > _this.navWidth - (parseInt(_this.navSlideWidth) + _this.clientWidth) / 2) {
                  navSwiper.setTranslate(_this.clientWidth - _this.navWidth)
                } else {
                  navSwiper.setTranslate((_this.clientWidth - parseInt(_this.navSlideWidth)) / 2 - navActiveSlideLeft)
                }

              },
            }
          });

          navSwiper.$el.on('touchstart', function(e) {
            e.preventDefault() //去掉按压阴影
          })
          navSwiper.on('tap', function(e) {

            var clickIndex = this.clickedIndex
            var clickSlide = this.slides.eq(clickIndex)
            pageSwiper.slideTo(clickIndex, 0);
            this.slides.find('span').css('color', 'rgba(51,51,51,1)');
            clickSlide.find('span').css('color', 'rgba(255,72,145,1)');
          })



          //内容滚动
          var scrollSwiper = new Swiper('.scroll', {
            //65是头部的高
            //36是top地址和搜索的高
            observer:true,//修改swiper自己或子元素时，自动初始化swiper
            slidesOffsetBefore: 72,
            direction: 'vertical',
            freeMode: true,
            slidesPerView: 1,
            autoHeight:true,
            mousewheel: {
              releaseOnEdges: true,
            },
            on: {
              touchMove: function() {
                if (this.translate > 72 - 36 && this.translate < 72) {
                  _this.topBar.transform('translateY(' + (this.translate - 72) + 'px)');
                }
              },
              touchStart: function() {
                _this.startPosition = this.translate
              },
              touchEnd: function() {
                _this.topBar.transition(tSpeed)
                if (this.translate > 36 && this.translate < 72 && this.translate < startPosition) {
                  _this.topBar.transform('translateY(-36px)');
                  for (var sc = 0; sc < scrollSwiper.length; sc++) {
                    if (scrollSwiper[sc].translate > 36) {
                      scrollSwiper[sc].setTransition(tSpeed);
                      scrollSwiper[sc].setTranslate(36)
                    }
                  }
                }
                if (this.translate > 36 && this.translate < 72 && this.translate > startPosition) {
                  _this.topBar.transform('translateY(0px)');
                  for (var sc = 0; sc < scrollSwiper.length; sc++) {
                    if (scrollSwiper[sc].translate < 72 && scrollSwiper[sc].translate > 0) {
                      scrollSwiper[sc].setTransition(tSpeed);
                      scrollSwiper[sc].setTranslate(72)
                    }
                  }
                }
              },

              transitionStart: function() {

                _this.topBar.transition(tSpeed)
                if (this.translate < 72 - 36) {
                  _this.topBar.transform('translateY(-36px)');
                  if (scrollSwiper) {
                    for (var sc = 0; sc < scrollSwiper.length; sc++) {
                      if (scrollSwiper[sc].translate > 36) {
                        scrollSwiper[sc].setTransition(tSpeed);
                        scrollSwiper[sc].setTranslate(36)
                      }
                    }
                  }

                } else {
                  _this.topBar.transform('translateY(0px)');

                  if (scrollSwiper) {
                    for (var sc = 0; sc < scrollSwiper.length; sc++) {
                      if (scrollSwiper[sc].translate < 72 && scrollSwiper[sc].translate > 0) {
                        scrollSwiper[sc].setTransition(tSpeed);
                        scrollSwiper[sc].setTranslate(72)
                      }
                    }
                  }
                }
              },
            }

          })


          //热卖
          var bannerSwiper = new Swiper('.banner', {

          });
        }
    },
    mounted(){
      this.nav()
    }
  }
</script>



